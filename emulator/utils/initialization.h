/*
* Functions related to initializing state and memory
*/

#pragma once

#include "../state.h"


namespace gb {

inline void setPostBootState (State &state) {
  state.A = 0x01;
  state.B = 0x00;
  state.C = 0x13;
  state.D = 0x00;
  state.E = 0xD8;
  state.F = 0x80;
  state.H = 0x01;
  state.L = 0x4D;
  state.SP = 0xFFFE;
  state.PC = 0x0100;

  static constexpr Byte initial_io_registers[0x48] = {
  //0x00   0x01  0x02  0x03  0x04  0x05  0x06  0x07
    0xCF,  0x00, 0x7E, 0x00, 0x18, 0x00, 0x00, 0xF8, // $FF00 - $FF07
    0x00,  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xE1, // $FF08 - $FF0F
    0x80,  0xBF, 0xF3, 0xFF, 0xBF, 0x00, 0x3F, 0x00, // $FF10 - $FF17
    0xFF,  0xBF, 0x7F, 0xFF, 0x9F, 0xFF, 0xBF, 0x00, // $FF18 - $FF1F
    0xFF,  0x00, 0x00, 0xBF, 0x77, 0xF3, 0xF1, 0x00, // $FF20 - $FF27
    0x00,  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // $FF28 - $FF2F
    0x00,  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // $FF30 - $FF37
    0x00,  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // $FF38 - $FF3F
    0x91,  0x81, 0x00, 0x00, 0x91, 0x00, 0xFF, 0xFC  // $FF40 - $FF47
  };

  for (Short i = 0; i < 0x48; i++) {
    state.memory.f(0xFF00 + i) = initial_io_registers[i];
  }
}

} // namespace gb